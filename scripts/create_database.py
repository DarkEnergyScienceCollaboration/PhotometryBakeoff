import numpy as np

def create_unique_id(sedName, redshift):
    """
    Accepts an sedName(string) and a redshift (float), and returns
    a unique id (int)

    Note: the redshift must be an integer multiple of 0.1
    """
    if sedName not in _hard_coded_name_dict:
        raise RuntimeError("Sed name %s is not an expected SED name; " % sedName
                           +" cannot return a unique id")

    baseline = _hard_coded_name_dict[sedName]

    redshift_dex = int(redshift/0.1)
    if np.abs(0.1*redshift_dex - redshift)>1.0e-5:
        raise RuntimeError("Redshift %e is not an integer multiple of 0.1; " % redshift
                           + "cannot return a unique id")


    return 1000*redshift_dex + baseline
